Cypress.Commands.add('getBaseUrl', (env, service) => {
    const urls = Cypress.env(env); // Access environment-specific URLs
    if (!urls || !urls[service]) {
      throw new Error(`URL not found for environment: ${env}, service: ${service}`);
    }
    return urls[service];
  });